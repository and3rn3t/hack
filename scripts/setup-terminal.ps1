        }
        "5" {
            Write-Host ""
            Write-Host "Setup cancelled." -ForegroundColor Yellow
            exit
        }
        default {
            Write-Host ""
            Write-Host "Invalid choice. Exiting." -ForegroundColor Red
            exit
        }
    }
}

